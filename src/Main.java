public class Main {
    public static void main(String[] args) {
        int num=3;
        System.out.println("The given number is: " + num);
        if (num % 2 == 0) {
            System.out.println(num + " is an even number.");
        } else {
            System.out.println(num + " is an odd number.");
        }
    }
}